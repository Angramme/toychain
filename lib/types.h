#ifndef __TYPES_H
#define __TYPES_H

/**
 * @file types.h
 * @brief a file with comfy aliases for long type signatures
 */

/**
 * @brief alias for long int
 */
#define int32 long int

/**
 * @brief alias for long long int
 */
#define int64 long long int

/**
 * @brief alias for unsigned long int
 */
#define uint32 unsigned long int

/**
 * @brief alias for unsigned long long int
 */
#define uint64 unsigned long long int

#endif